    private void loginButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_loginButtonMouseClicked
        // TODO add your handling code here:
        StudentPage studentPage = new StudentPage();
        LibrarianPage libPage = new LibrarianPage();
        if (emailLog.getText().equals("lib@au.edu") && passwordLog.getText().equals("lib123")){
            libPage.setVisible(true);
            dispose();
        } else {
            String query = "select * from user where Email = ? and Password = ?";

            try {
                pststmt = conn.prepareStatement(query);
                pststmt.setString(1, emailLog.getText());
                pststmt.setString(2, passwordLog.getText());

                rs = pststmt.executeQuery();

                int count = 0;

                while (rs.next()) {
                    count++;
                    HOLDID = rs.getString("ID");
                }

                if (count == 1) {

                    holdUser = emailLog.getText();
                    holdPassword = passwordLog.getText();

                    System.out.println(holdUser + " " + holdPassword);
                    System.out.println(HOLDID);

                    studentPage.setVisible(true);
                    dispose();

                } else if (count > 1) {

                    JOptionPane.showMessageDialog(null, "Duplicate Email and Password");

                } else {

                    JOptionPane.showMessageDialog(null, "Email or Password is Incorrect");

                }

                rs.close();
                pststmt.close();

            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_loginButtonMouseClicked

