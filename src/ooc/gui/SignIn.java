    public void checkDeleteID() {
        
        String query = "select * from reservation where now() >= adddate(str_to_date(day_of_reserve,'%d/%m/%Y'), interval 3 day)";
        try {
            Connection conn = DBConnector.connectDB();
            PreparedStatement pst = conn.prepareStatement(query);
            ResultSet rs = pst.executeQuery();
            while (rs.next()) {
                bookIDList.add(rs.getString("book_id"));
            }
            
            for (int i = 0; i < bookIDList.size(); i++) {
                System.out.println(bookIDList.get(i));
            }
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    public void changeAvailability() {
        try{
            Connection conn = DBConnector.connectDB();
            PreparedStatement pst = null;
            for (int i = 0; i < bookIDList.size(); i++) {
                String query = "update book set availability='Yes' where book_id='" + bookIDList.get(i) + "'";
                pst = conn.prepareStatement(query);
                pst.execute();
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
